<script>
  document.addEventListener('DOMContentLoaded', function() {
    const thumbnail = document.getElementById('flyerThumbnail');
    const lightbox = document.getElementById('flyerLightbox');
    const closeBtn = document.getElementById('lightboxClose');

    if (thumbnail && lightbox) {
      thumbnail.addEventListener('click', function() {
        lightbox.classList.add('active');
        document.body.style.overflow = 'hidden'; // Disable background scrolling
      });

      const closeLightbox = function() {
        lightbox.classList.remove('active');
        document.body.style.overflow = ''; // Restore background scrolling
      };

      closeBtn.addEventListener('click', function(e) {
        e.stopPropagation();
        closeLightbox();
      });

      lightbox.addEventListener('click', function(e) {
        // Close only if clicked directly on the dark background/overlay (not inner content)
        if (e.target === lightbox) {
          closeLightbox();
        }
      });

      // Support escape key to close
      document.addEventListener('keydown', function(e) {
        if (e.key === 'Escape' && lightbox.classList.contains('active')) {
          closeLightbox();
        }
      });
